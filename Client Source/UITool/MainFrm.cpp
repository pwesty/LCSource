void CMainFrame::SelectProperty(CUIBase* pUI, DWORD dwFlag)
{
	if (pUI == NULL)
		return;

	CUIBase* pParent = pUI->getParent();
	if (pUI->getType() == eUI_CONTROL_TEXT_EX ||
		(pParent != NULL && pParent->getType() == eUI_CONTROL_TEXT_EX))
		return;

	if( m_pOldSelect != NULL )
		m_pOldSelect->SetSelect( FALSE );

	m_pOldSelect = pUI;
	m_pOldSelect->SetSelect( TRUE );
	m_wndProperties.parseUI(pUI);
}


BOOL CMainFrame::OnSaveFile( const char* strPath )
{
	CUIManager* pUIMgr = CUIManager::getSingleton();

	CUIBase* pUI = pUIMgr->getChildAt(0);

	if( pUI )
	{
		XmlWrite* xml = new XmlWrite;

		if (strcmpi(strPath, "auto_save") == 0)
		{
			// 폴더 생성 명령어. 폴더가 존재 한다면 만들어 지지 않음.
			// 0 == 생성된 폴더가 있다, -1 == 에러 (생성 되지 않았다)
			if (_taccess("Backup Xml", 0) < 0)
				mkdir("Backup Xml");

			std::string strPath = "Backup Xml\\auto_save_" + theApp.getLoadFileName();

			xml->WriteXml(pUI, (char*)strPath.c_str());
			Message("AutoSave Complete. Path : %s", strPath);
		}
		else
		{
			xml->WriteXml(pUI, (char*)strPath);
		}
	}
	else
	{
		MessageBox("UI가 로딩되지 않았거나 생성된 UI가 없습니다.", "Error", 0 );
		return FALSE;
	}

	udpateRect();

	return TRUE;
}

void CMainFrame::AddUIControl( CString strControl )
{
	// 선택된 컨트롤 추가
	
	// m_pOldSelect에 선택된 컨트롤이 UIListItem이라면 Parent로 UIList을 넘겨준다.
	if (m_pOldSelect != NULL && m_pOldSelect->getType() == eUI_CONTROL_LIST_ITEM)
		m_wndFileView.CreateControl(m_pOldSelect, strControl);
	// UIArrayItem도 마찬가지.
	else if (m_pOldSelect != NULL && m_pOldSelect->getType() == eUI_CONTROL_ARRAY_ITEM)
		m_wndFileView.CreateControl(m_pOldSelect, strControl);
	else
		m_wndFileView.CreateControl(CUIManager::getSingleton()->getChildAt(0), strControl);

	OnSaveFile("auto_save");
}

void CMainFrame::Message( const char* message, ... )
{
	std::string strFormat = message;

	va_list args;
	va_start(args, message);
	char buffer[1025];
	_vsnprintf(buffer, 1024, strFormat.c_str(), args);
	m_wndOutput.Message(buffer);
}

void CMainFrame::udpateRect()
{
	//if( CMainFrame *pMain = (CMainFrame *)AfxGetMainWnd() )
	{
		if( CChildFrame *pChild = (CChildFrame *)GetActiveFrame() )
		{
			if( CUIToolView* pView = (CUIToolView*)pChild->GetActiveView() )
			{
				pView->Invalidate();
				pView->UpdateWindow();
			}
		}
	}
}

