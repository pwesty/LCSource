BOOL CMainFrame::OnLoadFile( const char* strPath )
{
	if( strPath == NULL )
		return FALSE;

	m_strPath = strPath;

	CUIManager* pUIMgr = CUIManager::getSingleton();
	CUIFocus::getSingleton()->setUI(NULL);
	ResetSelectUI();
	m_wndProperties.ResetSelectUI();

	if( pUIMgr )
	{
		pUIMgr->deleteChildAll();
		CUIFocus::getSingleton()->setUI(NULL);
		pUIMgr->LoadXML( strPath );

		initData();
	}

	return TRUE;
}

void CMainFrame::Refresh()
{
	if (m_strPath.empty() == false)
	{
		CmdOpenFile* pCmd = new CmdOpenFile;
		pCmd->setData(m_strPath.c_str());
		theApp.addQueue(pCmd);
	}
}

